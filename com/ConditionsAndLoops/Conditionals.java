package com.ConditionsAndLoops;

public class Conditionals{
    public static void main(String[] args) {

        int a = 10 ;
        int b= 21;

        if (a == 10 && b == 21){   // and
            System.out.println("hello");
        }
        int c = 10;
        int d = 54;

        if (c == 10 || d == 21){ // or
            System.out.println("hello");
        }
    }
}

